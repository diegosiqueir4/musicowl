package de.wwu.musicowl.core;

public class Key {

	private String fifths;
	private String mode;
	
	public Key() {
		super();
	
	}
	
	public String getFifths() {
		return fifths;
	}
	public void setFifths(String fifths) {
		this.fifths = fifths;
	}
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
	
	
}
