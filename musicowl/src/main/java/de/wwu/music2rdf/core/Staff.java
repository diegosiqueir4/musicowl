package de.wwu.music2rdf.core;

public class Staff {

	private String id;
	private String part;
	
	public Staff() {
		super();
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPart() {
		return part;
	}
	public void setPart(String part) {
		this.part = part;
	} 
	
	
	
}

